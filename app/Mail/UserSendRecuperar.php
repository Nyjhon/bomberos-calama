    public function __construct($data)
    {
        $this->data = $data;
    }

    /**
     * Build the message.
     *
     * @return $this
     */
    public function build()
    {
        return $this->from(env('MAIL_FROM_ADDRESS'), env('MAIL_FROM_NAME'))
        ->view('email.recuperar_contraseÃ±a')
        ->subject('Recuperar su contraseÃ±a')
        ->with($this->data);
    }
}
